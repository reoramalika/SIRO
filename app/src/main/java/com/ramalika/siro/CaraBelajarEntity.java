public class CaraBelajarEntity {
    private int ID;
    private String namaCaraBelajar;
    private String kategori;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getNamaCaraBelajar() {
        return namaCaraBelajar;
    }

    public void setNamaCaraBelajar(String namaCaraBelajar) {
        this.namaCaraBelajar = namaCaraBelajar;
    }

    public String getKategori() {
        return kategori;
    }

    public void setKategori(String kategori) {
        this.kategori = kategori;
    }
}
